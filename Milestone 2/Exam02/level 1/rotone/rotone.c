#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;
	char c;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			c = argv[1][i];
			if (('a' <= argv[1][i] && argv[1][i] <= 'z') || ('A' <= argv[1][i] && argv[1][i] <= 'Z'))
			{
				if (c == 'z')
					c = 'a';
				else if (c == 'Z')
					c = 'A';
				else
					c = c + 1;
			}
			write (1, &c, 1);
			i++;
		}
	}
	write (1, "\n", 1);
}
